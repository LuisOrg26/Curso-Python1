Farenheit = int(input("¿Cual es la temperatura en Farenheit?"))
Celsius = (Farenheit-32)*5/9
print("{}° Fahrenheit es igual a {}° Celsius".format(Farenheit,Celsius))

Libra = float(input("¿Cuantas libras deseas convertir? "))
Euro = Libra * 1.15
print("${} libras es igual a ${} euros".format(Libra,Euro))
